/**
 * NO2 用: edge-tts で各セリフを生成し実長を計測 → no2-video.constants.mjs を書き出す。
 *
 * 前提: pip install edge-tts、ネット接続、ffmpeg（PATH または WinGet 版）
 *
 * 環境変数:
 *   NO2_NARRATION_VOICE  既定 ja-JP-NanamiNeural
 *   NO2_VOICE_RATE       既定 +28%
 *   NO2_TIMELINE_PAD_MS  各セグメント末尾に足す余白 ms（既定 240）
 *   NO2_TIMELINE_MIN_MS  表示の最短 ms（既定 900）
 */
import { spawn } from "node:child_process";
import fs from "node:fs/promises";
import path from "node:path";
import { NO2_NARRATION_SEGMENTS } from "./no2-narration-segments.mjs";
import { readAudioDurationSeconds } from "./read-audio-duration-ffmpeg.mjs";

const ROOT = process.cwd();
const OUT_FILE = path.join(ROOT, "no2-video.constants.mjs");
const WORK_DIR = path.join(ROOT, "output", "no2-voice-work");
const EDGE_VOICE = process.env.NO2_NARRATION_VOICE ?? "ja-JP-NanamiNeural";
const EDGE_RATE = process.env.NO2_VOICE_RATE ?? "+28%";
const PAD_MS = Number(process.env.NO2_TIMELINE_PAD_MS ?? "240");
const MIN_MS = Number(process.env.NO2_TIMELINE_MIN_MS ?? "900");

function runCommand(command, args, options = {}) {
  return new Promise((resolve, reject) => {
    const child = spawn(command, args, {
      cwd: ROOT,
      stdio: "inherit",
      shell: process.platform === "win32",
      ...options,
    });
    child.on("error", reject);
    child.on("exit", (code) => {
      if (code === 0) resolve();
      else reject(new Error(`${command} ${args.join(" ")} failed (code ${code})`));
    });
  });
}

async function resolveFfmpegCommand() {
  const candidates = [];
  if (process.env.FFMPEG_PATH) candidates.push(process.env.FFMPEG_PATH);

  if (process.env.LOCALAPPDATA) {
    candidates.push(
      path.join(
        process.env.LOCALAPPDATA,
        "Microsoft",
        "WinGet",
        "Packages",
        "Gyan.FFmpeg_Microsoft.Winget.Source_8wekyb3d8bbwe",
        "ffmpeg-8.1-full_build",
        "bin",
        "ffmpeg.exe",
      ),
    );
  }

  for (const candidate of candidates) {
    try {
      await fs.access(candidate);
      return candidate;
    } catch {
      // try next
    }
  }
  return "ffmpeg";
}

async function edgeTtsToMp3(text, outMp3) {
  const txtPath = `${outMp3}.txt`;
  await fs.writeFile(txtPath, `${text}\n`, "utf8");
  await runCommand(
    "python",
    ["-m", "edge_tts", "-f", txtPath, "-v", EDGE_VOICE, "--rate", EDGE_RATE, "--write-media", outMp3],
    { shell: false },
  );
}

function clampMs(ms) {
  const padded = Math.ceil(ms) + (Number.isFinite(PAD_MS) ? PAD_MS : 240);
  const floor = Number.isFinite(MIN_MS) ? MIN_MS : 900;
  return Math.max(floor, padded);
}

async function main() {
  await fs.mkdir(WORK_DIR, { recursive: true });
  const ffmpeg = await resolveFfmpegCommand();

  const displayMsList = [];
  for (let i = 0; i < NO2_NARRATION_SEGMENTS.length; i += 1) {
    const text = NO2_NARRATION_SEGMENTS[i];
    const rawMp3 = path.join(WORK_DIR, `timeline-probe-${String(i).padStart(2, "0")}.mp3`);
    console.log(`[${i + 1}/${NO2_NARRATION_SEGMENTS.length}] TTS 生成・計測 …`);
    await edgeTtsToMp3(text, rawMp3);
    const rawSec = await readAudioDurationSeconds(ffmpeg, rawMp3);
    if (rawSec == null || !Number.isFinite(rawSec) || rawSec <= 0) {
      throw new Error(`音声長の取得に失敗しました: ${rawMp3}（ffmpeg: ${ffmpeg}）`);
    }
    const ms = clampMs(rawSec * 1000);
    displayMsList.push(ms);
    console.log(`    生 ${rawSec.toFixed(3)}s → 表示 ${ms}ms（+pad ${PAD_MS}ms, min ${MIN_MS}ms）`);
  }

  if (displayMsList.length !== 7 && displayMsList.length !== 8) {
    throw new Error(
      `NO2_NARRATION_SEGMENTS は 7 文（アウトロなし）または 8 文（アウトロあり）である必要があります。現在: ${displayMsList.length} 文`,
    );
  }

  const [profileMs, beforeMs, transitionMs, dash0, dash1, dash2, dash3] = displayMsList;
  const outroMs = displayMsList[7] ?? 0;

  const afterMs = dash0 + dash1 + dash2 + dash3;
  const movieMs = profileMs + beforeMs + transitionMs + afterMs + outroMs;

  const cycleSec = [dash0, dash1, dash2, dash3].map((ms) => Math.max(0.5, ms / 1000));

  const body = `/** Auto-generated by scripts/prepare-no2-voice-timeline.mjs — 台本変更後は npm run prep:no2-voice-timeline */
export const NO2_PROFILE_SCENE_MS = ${profileMs};
export const NO2_BEFORE_SCENE_MS = ${beforeMs};
export const NO2_TRANSITION_SCENE_MS = ${transitionMs};
/** ダッシュ「after」内の各タブ表示時間（ms）。合計が NO2_AFTER_SCENE_MS */
export const NO2_DASHBOARD_TICK_MS = [${dash0}, ${dash1}, ${dash2}, ${dash3}];
export const NO2_AFTER_SCENE_MS = ${afterMs};
export const NO2_OUTRO_SCENE_MS = ${outroMs};
/** 各ダッシュスライドの dashSlideCycle 秒（ticks と一致） */
export const NO2_DASH_SLIDE_CYCLE_SEC = [${cycleSec.map((s) => Number(s.toFixed(4))).join(", ")}];
/** Playwright 収録・ffmpeg -t 用の総尺 */
export const NO2_MOVIE_DURATION_MS = ${movieMs};
`;

  await fs.writeFile(OUT_FILE, body, "utf8");
  console.log(`\n書き出し: ${OUT_FILE}`);
  console.log(`NO2_MOVIE_DURATION_MS = ${movieMs}（約 ${(movieMs / 1000).toFixed(2)} 秒）`);
}

main().catch((err) => {
  console.error(err);
  console.error("\nヒント: pip install edge-tts、ffmpeg、ネット接続を確認してください。");
  process.exitCode = 1;
});

