/**
 * NO5: TTS 2セグメント → no5-video.constants.mjs の PROFILE / AFTER / MOVIE_DURATION を更新。
 * NO5_FORM_CAL_VIDEO_PLAY_DELAY_MS は既存値を保持。
 */
import { spawn } from "node:child_process";
import fs from "node:fs/promises";
import path from "node:path";
import { NO5_NARRATION_SEGMENTS } from "./no5-narration-segments.mjs";
import { readAudioDurationSeconds } from "./read-audio-duration-ffmpeg.mjs";

const ROOT = process.cwd();
const CONST_FILE = path.join(ROOT, "no5-video.constants.mjs");
const WORK_DIR = path.join(ROOT, "output", "no5-voice-work");
const EDGE_VOICE = process.env.NO5_NARRATION_VOICE ?? "ja-JP-NanamiNeural";
const EDGE_RATE = process.env.NO5_VOICE_RATE ?? "+18%";
const PAD_MS = Number(process.env.NO5_TIMELINE_PAD_MS ?? "240");
const MIN_MS = Number(process.env.NO5_TIMELINE_MIN_MS ?? "900");
const AFTER_SCENE_MIN_MS = Number(process.env.NO5_AFTER_SCENE_MIN_MS ?? "36000");

function parseExport(text, name) {
  const m = text.match(new RegExp(`export const ${name} = ([^;\\n]+);`));
  return m ? m[1].trim() : null;
}

function runCommand(command, args, options = {}) {
  return new Promise((resolve, reject) => {
    const child = spawn(command, args, {
      cwd: ROOT,
      stdio: "inherit",
      shell: process.platform === "win32",
      ...options,
    });
    child.on("error", reject);
    child.on("exit", (code) => {
      if (code === 0) resolve();
      else reject(new Error(`${command} ${args.join(" ")} failed (code ${code})`));
    });
  });
}

async function resolveFfmpegCommand() {
  const candidates = [];
  if (process.env.FFMPEG_PATH) candidates.push(process.env.FFMPEG_PATH);
  if (process.env.LOCALAPPDATA) {
    candidates.push(
      path.join(
        process.env.LOCALAPPDATA,
        "Microsoft",
        "WinGet",
        "Packages",
        "Gyan.FFmpeg_Microsoft.Winget.Source_8wekyb3d8bbwe",
        "ffmpeg-8.1-full_build",
        "bin",
        "ffmpeg.exe",
      ),
    );
  }
  for (const candidate of candidates) {
    try {
      await fs.access(candidate);
      return candidate;
    } catch {
      // continue
    }
  }
  return "ffmpeg";
}

async function edgeTtsToMp3(text, outMp3) {
  const txtPath = `${outMp3}.txt`;
  await fs.writeFile(txtPath, `${text}\n`, "utf8");
  await runCommand(
    "python",
    ["-m", "edge_tts", "-f", txtPath, "-v", EDGE_VOICE, "--rate", EDGE_RATE, "--write-media", outMp3],
    { shell: false },
  );
}

function clampMs(ms) {
  const padded = Math.ceil(ms) + (Number.isFinite(PAD_MS) ? PAD_MS : 240);
  const floor = Number.isFinite(MIN_MS) ? MIN_MS : 900;
  return Math.max(floor, padded);
}

async function main() {
  const raw = await fs.readFile(CONST_FILE, "utf8");
  const formDelay = parseExport(raw, "NO5_FORM_CAL_VIDEO_PLAY_DELAY_MS");
  if (!formDelay) {
    throw new Error("no5-video.constants.mjs に NO5_FORM_CAL_VIDEO_PLAY_DELAY_MS がありません。");
  }

  await fs.mkdir(WORK_DIR, { recursive: true });
  const ffmpeg = await resolveFfmpegCommand();

  const displayMsList = [];
  for (let i = 0; i < NO5_NARRATION_SEGMENTS.length; i += 1) {
    const text = NO5_NARRATION_SEGMENTS[i];
    const rawMp3 = path.join(WORK_DIR, `timeline-probe-${String(i).padStart(2, "0")}.mp3`);
    console.log(`[${i + 1}/${NO5_NARRATION_SEGMENTS.length}] TTS 生成・計測 …`);
    await edgeTtsToMp3(text, rawMp3);
    const rawSec = await readAudioDurationSeconds(ffmpeg, rawMp3);
    if (rawSec == null || !Number.isFinite(rawSec) || rawSec <= 0) {
      throw new Error(`音声長の取得に失敗: ${rawMp3}`);
    }
    const ms = clampMs(rawSec * 1000);
    displayMsList.push(ms);
    console.log(`    生 ${rawSec.toFixed(3)}s → 表示 ${ms}ms`);
  }

  const [profileMs, afterMsRaw] = displayMsList;
  const afterFloor = Number.isFinite(AFTER_SCENE_MIN_MS) ? Math.max(0, AFTER_SCENE_MIN_MS) : 36000;
  const afterMs = Math.max(afterFloor, afterMsRaw);
  if (afterMs > afterMsRaw) {
    console.log(`    afterシーンを最低尺 ${afterFloor}ms に拡張（計測値 ${afterMsRaw}ms）`);
  }
  const movieMs = profileMs + afterMs;

  const body = `/** Auto-generated by scripts/prepare-no5-voice-timeline.mjs — 台本変更後は npm run prep:no5-voice-timeline */
export const NO5_PROFILE_SCENE_MS = ${profileMs};
export const NO5_AFTER_SCENE_MS = ${afterMs};
export const NO5_FORM_CAL_VIDEO_PLAY_DELAY_MS = ${formDelay};
/** profile + after（収録・音声の合計尺） */
export const NO5_MOVIE_DURATION_MS = ${movieMs};
`;

  await fs.writeFile(CONST_FILE, body, "utf8");
  console.log(`\n書き出し: ${CONST_FILE}`);
  console.log(`NO5_MOVIE_DURATION_MS = ${movieMs}（約 ${(movieMs / 1000).toFixed(2)} 秒）`);
}

main().catch((err) => {
  console.error(err);
  process.exitCode = 1;
});

