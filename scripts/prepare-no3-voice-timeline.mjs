/**
 * NO3 用: edge-tts で各セリフを生成し実長を計測 → no3-video.constants.mjs を書き出す。
 * 4シーン（profile / before / transition / after）。アウトロなし。
 *
 * 環境変数:
 *   NO3_NARRATION_VOICE  既定 ja-JP-NanamiNeural
 *   NO3_VOICE_RATE       既定 +24%
 *   NO3_TIMELINE_PAD_MS  既定 240
 *   NO3_TIMELINE_MIN_MS  既定 900
 *   NO3_AFTER_SCENE_MIN_MS 既定 32000（LP実機収録を十分に見せる最低尺）
 */
import { spawn } from "node:child_process";
import fs from "node:fs/promises";
import path from "node:path";
import { NO3_NARRATION_SEGMENTS } from "./no3-narration-segments.mjs";
import { readAudioDurationSeconds } from "./read-audio-duration-ffmpeg.mjs";

const ROOT = process.cwd();
const OUT_FILE = path.join(ROOT, "no3-video.constants.mjs");
const WORK_DIR = path.join(ROOT, "output", "no3-voice-work");
const EDGE_VOICE = process.env.NO3_NARRATION_VOICE ?? "ja-JP-NanamiNeural";
const EDGE_RATE = process.env.NO3_VOICE_RATE ?? "+24%";
/** 既定をやや大きめにし、各シーンでナレーションが余裕を持って収まるようにする（尺は長くなってよい） */
const PAD_MS = Number(process.env.NO3_TIMELINE_PAD_MS ?? "700");
const MIN_MS = Number(process.env.NO3_TIMELINE_MIN_MS ?? "900");
const AFTER_SCENE_MIN_MS = Number(process.env.NO3_AFTER_SCENE_MIN_MS ?? "23000");

function runCommand(command, args, options = {}) {
  return new Promise((resolve, reject) => {
    const child = spawn(command, args, {
      cwd: ROOT,
      stdio: "inherit",
      shell: process.platform === "win32",
      ...options,
    });
    child.on("error", reject);
    child.on("exit", (code) => {
      if (code === 0) resolve();
      else reject(new Error(`${command} ${args.join(" ")} failed (code ${code})`));
    });
  });
}

async function resolveFfmpegCommand() {
  const candidates = [];
  if (process.env.FFMPEG_PATH) candidates.push(process.env.FFMPEG_PATH);

  if (process.env.LOCALAPPDATA) {
    candidates.push(
      path.join(
        process.env.LOCALAPPDATA,
        "Microsoft",
        "WinGet",
        "Packages",
        "Gyan.FFmpeg_Microsoft.Winget.Source_8wekyb3d8bbwe",
        "ffmpeg-8.1-full_build",
        "bin",
        "ffmpeg.exe",
      ),
    );
  }

  for (const candidate of candidates) {
    try {
      await fs.access(candidate);
      return candidate;
    } catch {
      // try next
    }
  }
  return "ffmpeg";
}

async function edgeTtsToMp3(text, outMp3) {
  const txtPath = `${outMp3}.txt`;
  await fs.writeFile(txtPath, `${text}\n`, "utf8");
  await runCommand(
    "python",
    ["-m", "edge_tts", "-f", txtPath, "-v", EDGE_VOICE, "--rate", EDGE_RATE, "--write-media", outMp3],
    { shell: false },
  );
}

function clampMs(ms) {
  const padded = Math.ceil(ms) + (Number.isFinite(PAD_MS) ? PAD_MS : 240);
  const floor = Number.isFinite(MIN_MS) ? MIN_MS : 900;
  return Math.max(floor, padded);
}

async function main() {
  await fs.mkdir(WORK_DIR, { recursive: true });
  const ffmpeg = await resolveFfmpegCommand();

  const displayMsList = [];
  for (let i = 0; i < NO3_NARRATION_SEGMENTS.length; i += 1) {
    const text = NO3_NARRATION_SEGMENTS[i];
    const rawMp3 = path.join(WORK_DIR, `timeline-probe-${String(i).padStart(2, "0")}.mp3`);
    console.log(`[${i + 1}/${NO3_NARRATION_SEGMENTS.length}] TTS 生成・計測 …`);
    await edgeTtsToMp3(text, rawMp3);
    const rawSec = await readAudioDurationSeconds(ffmpeg, rawMp3);
    if (rawSec == null || !Number.isFinite(rawSec) || rawSec <= 0) {
      throw new Error(`音声長の取得に失敗しました: ${rawMp3}（ffmpeg: ${ffmpeg}）`);
    }
    const ms = clampMs(rawSec * 1000);
    displayMsList.push(ms);
    console.log(`    生 ${rawSec.toFixed(3)}s → 表示 ${ms}ms（+pad ${PAD_MS}ms, min ${MIN_MS}ms）`);
  }

  const [profileMs, beforeMs, transitionMs, afterMsRaw] = displayMsList;
  const afterFloor = Number.isFinite(AFTER_SCENE_MIN_MS) ? Math.max(0, AFTER_SCENE_MIN_MS) : 32000;
  const afterMs = Math.max(afterFloor, afterMsRaw);
  if (afterMs > afterMsRaw) {
    console.log(`    afterシーンを最低尺 ${afterFloor}ms に拡張（計測値 ${afterMsRaw}ms）`);
  }
  const movieMs = profileMs + beforeMs + transitionMs + afterMs;

  const body = `/** Auto-generated by scripts/prepare-no3-voice-timeline.mjs — 台本変更後は npm run prep:no3-voice-timeline */
export const NO3_PROFILE_SCENE_MS = ${profileMs};
export const NO3_BEFORE_SCENE_MS = ${beforeMs};
export const NO3_TRANSITION_SCENE_MS = ${transitionMs};
export const NO3_AFTER_SCENE_MS = ${afterMs};
export const NO3_MOVIE_DURATION_MS = ${movieMs};
`;

  await fs.writeFile(OUT_FILE, body, "utf8");
  console.log(`\n書き出し: ${OUT_FILE}`);
  console.log(`NO3_MOVIE_DURATION_MS = ${movieMs}（約 ${(movieMs / 1000).toFixed(2)} 秒）`);
}

main().catch((err) => {
  console.error(err);
  console.error("\nヒント: pip install edge-tts、ffmpeg、ネット接続を確認してください。");
  process.exitCode = 1;
});

