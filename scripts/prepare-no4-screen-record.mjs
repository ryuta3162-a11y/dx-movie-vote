import { spawn } from "node:child_process";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { readMp4DurationSeconds } from "./read-mp4-duration.mjs";

const ROOT = process.cwd();
const SRC = path.join(ROOT, "public", "no4", "tool-screen-record.mp4");
const OUT = path.join(ROOT, "public", "no4", "tool-screen-record-cropped.mp4");
const CONST_OUT = path.join(ROOT, "no4-video.constants.mjs");

const PLAYBACK = 1.2;
/**
 * 全画面収録ではタブ〜ブックマーク帯だけで 150px 超えることが多く、数十 px のカットでは「カットされていない」ように見える。
 * 既定は高さに対する割合（1080p 実機でもブラウザ上端・タスクバー下端を落としやすい量）。
 */
const TOP_FRAC = Number(process.env.NO4_CROP_TOP_FRAC ?? "0.17");
const BOTTOM_FRAC = Number(process.env.NO4_CROP_BOTTOM_FRAC ?? "0.11");
const SRC_HEIGHT = Number(process.env.NO4_SRC_HEIGHT ?? "1080");
const AFTER_MARGIN_MS = Number(process.env.NO4_AFTER_MARGIN_MS ?? "4500");
const MOVIE_TAIL_MS = Number(process.env.NO4_MOVIE_TAIL_MS ?? "2500");
/** App.jsx NO4「課題（DX前）」シーンの表示時間（ms）。4枚×約5秒＋余白用に既定を長めに。 */
const NO4_BEFORE_SCENE_MS = Number(process.env.NO4_BEFORE_SCENE_MS ?? "27000");
/** 収録末尾に不要画が入った場合など、ソース MP4 の終端から切り捨てる秒数（0 で無効） */
const TRIM_END_SEC = Math.max(0, Number(process.env.NO4_TRIM_END_SEC ?? "20"));

function cropPixelsFromFractions(height) {
  let topPx = Math.floor(height * TOP_FRAC);
  let botPx = Math.floor(height * BOTTOM_FRAC);
  let outH = height - topPx - botPx;
  if (outH < 2) throw new Error("Crop fractions too large for source height.");
  if (outH % 2 !== 0) {
    botPx += 1;
    outH = height - topPx - botPx;
  }
  return { topPx, botPx, outH };
}

function runCommand(command, args, options = {}) {
  return new Promise((resolve, reject) => {
    const child = spawn(command, args, {
      cwd: ROOT,
      stdio: "inherit",
      shell: process.platform === "win32",
      ...options,
    });
    child.on("error", reject);
    child.on("exit", (code) => {
      if (code === 0) resolve();
      else reject(new Error(`${command} failed (${code})`));
    });
  });
}

async function resolveFfmpegCommand() {
  const candidates = [];
  if (process.env.FFMPEG_PATH) candidates.push(process.env.FFMPEG_PATH);
  if (process.env.LOCALAPPDATA) {
    const wingetFfmpegPath = path.join(
      process.env.LOCALAPPDATA,
      "Microsoft",
      "WinGet",
      "Packages",
      "Gyan.FFmpeg_Microsoft.Winget.Source_8wekyb3d8bbwe",
      "ffmpeg-8.1-full_build",
      "bin",
      "ffmpeg.exe",
    );
    candidates.push(wingetFfmpegPath);
  }
  for (const candidate of candidates) {
    try {
      await fs.access(candidate);
      return candidate;
    } catch {
      // continue
    }
  }
  return "ffmpeg";
}

async function ffmpegCanEncodeH264(ffmpegCommand) {
  return new Promise((resolve) => {
    const child = spawn(ffmpegCommand, ["-hide_banner", "-encoders"], {
      shell: process.platform === "win32",
    });
    let out = "";
    child.stdout?.on("data", (d) => {
      out += d.toString();
    });
    child.stderr?.on("data", (d) => {
      out += d.toString();
    });
    child.on("error", () => resolve(false));
    child.on("exit", (code) => {
      if (code !== 0) resolve(false);
      else resolve(/\blibx264\b/.test(out));
    });
  });
}

async function tryCropWithFfmpeg(ffmpegCommand, durationLimitSec) {
  const { topPx, botPx, outH } = cropPixelsFromFractions(SRC_HEIGHT);
  const vf = `crop=iw:${outH}:0:${topPx}`;
  console.log(
    `Crop: top ${topPx}px (~${(TOP_FRAC * 100).toFixed(1)}%), bottom ${botPx}px (~${(BOTTOM_FRAC * 100).toFixed(1)}%), out height ${outH}px (assumed source H=${SRC_HEIGHT})`,
  );
  if (Number.isFinite(durationLimitSec) && durationLimitSec > 0) {
    console.log(`Encode length cap: ${durationLimitSec}s (from trimmed source)`);
  }
  try {
    await runCommand(
      ffmpegCommand,
      [
        "-y",
        "-i",
        SRC,
        ...(Number.isFinite(durationLimitSec) && durationLimitSec > 0 ? ["-t", String(durationLimitSec)] : []),
        "-vf",
        vf,
        "-an",
        "-c:v",
        "libx264",
        "-crf",
        process.env.NO4_CRF ?? "18",
        "-preset",
        process.env.NO4_PRESET ?? "medium",
        "-pix_fmt",
        "yuv420p",
        "-movflags",
        "+faststart",
        OUT,
      ],
      { shell: false },
    );
    return true;
  } catch {
    return false;
  }
}

const NO4_PROFILE_SCENE_MS_DEFAULT = 4500;

function writeConstants({ durationSec, videoSrc, visualClipInset, cropNote, trimNote }) {
  const afterMs = Math.ceil((durationSec / PLAYBACK) * 1000) + AFTER_MARGIN_MS;
  const movieMs = NO4_PROFILE_SCENE_MS_DEFAULT + NO4_BEFORE_SCENE_MS + afterMs + MOVIE_TAIL_MS;
  const body = `/** Auto-generated by scripts/prepare-no4-screen-record.mjs — run: npm run prep:no4-video */
export const NO4_PLAYBACK_RATE = ${PLAYBACK};
export const NO4_SCREEN_VIDEO_SRC = ${JSON.stringify(videoSrc)};
/** CSS clip when re-encoded crop is unavailable. ${cropNote} */
export const NO4_VISUAL_CLIP_INSET = ${JSON.stringify(visualClipInset)};
/** 実際に使う尺（秒）。${trimNote} */
export const NO4_SOURCE_DURATION_SEC = ${Number(durationSec.toFixed(4))};
/** プロフィールシーン（ms）。音声タイムラインで上書き可 */
export const NO4_PROFILE_SCENE_MS = ${NO4_PROFILE_SCENE_MS_DEFAULT};
/** 収録末尾の余白（ms）。音声タイムラインの総尺計算に使用 */
export const NO4_MOVIE_TAIL_MS = ${MOVIE_TAIL_MS};
/** NO4「課題（DX前）」シーン長（ms）。音声後乗せ用に余裕あり。環境変数 NO4_BEFORE_SCENE_MS で変更可。 */
export const NO4_BEFORE_SCENE_MS = ${NO4_BEFORE_SCENE_MS};
export const NO4_AFTER_SCENE_MS = ${afterMs};
export const NO4_MOVIE_DURATION_MS = ${movieMs};
`;
  return fs.writeFile(CONST_OUT, body, "utf8");
}

async function main() {
  await fs.access(SRC);
  const rawDurationSec = await readMp4DurationSeconds(SRC);
  const durationSec = Math.max(0.5, rawDurationSec - TRIM_END_SEC);
  if (TRIM_END_SEC > 0) {
    console.log(
      `Source file duration ${rawDurationSec.toFixed(2)}s; NO4_TRIM_END_SEC=${TRIM_END_SEC} → using ${durationSec.toFixed(2)}s for encode & timeline.`,
    );
  }
  if (durationSec < 2) {
    throw new Error(
      `Effective duration too short (${durationSec}s). Reduce NO4_TRIM_END_SEC or replace source video.`,
    );
  }
  if (TRIM_END_SEC > 0 && rawDurationSec <= TRIM_END_SEC) {
    throw new Error(`Source (${rawDurationSec}s) is shorter than trim (${TRIM_END_SEC}s).`);
  }

  const ffmpegCommand = await resolveFfmpegCommand();
  const canEncode = await ffmpegCanEncodeH264(ffmpegCommand);

  if (TRIM_END_SEC > 0 && !canEncode) {
    throw new Error(
      "Trimming the end of the video requires FFmpeg with libx264. Install full FFmpeg or set FFMPEG_PATH.",
    );
  }

  let videoSrc = "/no4/tool-screen-record.mp4";
  const { topPx, botPx } = cropPixelsFromFractions(SRC_HEIGHT);
  const cropNote = `Target ~top ${(TOP_FRAC * 100).toFixed(0)}% / bottom ${(BOTTOM_FRAC * 100).toFixed(0)}% (${topPx}px+${botPx}px @ H=${SRC_HEIGHT}).`;
  const trimNote =
    TRIM_END_SEC > 0
      ? `末尾 ${TRIM_END_SEC}s を除去（元ファイル ${rawDurationSec.toFixed(2)}s）`
      : "ソースファイル全尺";
  let visualClipInset = `inset(${(TOP_FRAC * 100).toFixed(2)}% 0 ${(BOTTOM_FRAC * 100).toFixed(2)}% 0)`;

  if (canEncode) {
    console.log(`Using ffmpeg: ${ffmpegCommand}`);
    const ok = await tryCropWithFfmpeg(ffmpegCommand, durationSec);
    if (ok) {
      videoSrc = "/no4/tool-screen-record-cropped.mp4";
      visualClipInset = "inset(0)";
      console.log(`Wrote cropped video: ${path.relative(ROOT, OUT)}`);
    } else {
      console.warn("ffmpeg crop failed; keeping source file and CSS clip only.");
    }
  } else {
    console.warn(
      "No libx264-capable ffmpeg found (install full FFmpeg or set FFMPEG_PATH). Using source MP4 + CSS clip-path.",
    );
  }

  await writeConstants({ durationSec, videoSrc, visualClipInset, cropNote, trimNote });
  console.log(`Wrote ${path.relative(ROOT, CONST_OUT)}`);
  console.log(`NO4_AFTER_SCENE_MS (1.2× + margin): ${Math.ceil((durationSec / PLAYBACK) * 1000) + AFTER_MARGIN_MS}`);
}

const isMain = process.argv[1] === fileURLToPath(import.meta.url);
if (isMain) {
  main().catch((e) => {
    console.error(e);
    process.exitCode = 1;
  });
}
